	#region AOT Bootstrap Emission (Pipeline 2)

	/// <summary>
	/// Emits a [ModuleInitializer] that registers every concrete Component in
	/// ComponentAotFactory. This is all that's needed — serialization is handled
	/// by Voltage.Persistence.Json + the generated Data property, not STJ.
	/// </summary>
	private static void EmitAotBootstrap(SourceProductionContext spc, ImmutableArray<BootstrapEntry> components)
	{
		if (components.IsDefaultOrEmpty)
			return;

		var unique = components
			.GroupBy(c => c.ComponentFullName)
			.Select(g => g.First())
			.ToList();

		var sb = new StringBuilder();
		sb.AppendLine("// <auto-generated/>");
		sb.AppendLine("#nullable disable");
		sb.AppendLine("using Voltage.Serialization;");
		sb.AppendLine("using Voltage.Serialization.Registries;");
		sb.AppendLine();

		var namespaces = unique
			.Select(c => c.Namespace)
			.Where(n => n != null)
			.Distinct()
			.OrderBy(n => n);
		foreach (var ns in namespaces)
			sb.AppendLine($"using {ns};");

		sb.AppendLine();
		sb.AppendLine("namespace Voltage.Serialization.Generated");
		sb.AppendLine("{");
		sb.AppendLine("\tinternal static class ComponentDataAotBootstrap");
		sb.AppendLine("\t{");
		sb.AppendLine("\t\tprivate static bool _inited;");
		sb.AppendLine();
		sb.AppendLine("\t\t[System.Runtime.CompilerServices.ModuleInitializer]");
		sb.AppendLine("\t\tinternal static void AutoRegister()");
		sb.AppendLine("\t\t{");
		sb.AppendLine("\t\t\tif (_inited) return;");
		sb.AppendLine("\t\t\t_inited = true;");
		sb.AppendLine();

		// VLT004 — two distinct components must never share a [ComponentId]. This usually means a
		// component file was copy-pasted without changing the id; resolution would be ambiguous.
		var idOwners = new Dictionary<string, List<BootstrapEntry>>(StringComparer.Ordinal);
		foreach (var entry in unique)
		{
			if (string.IsNullOrEmpty(entry.ComponentId))
				continue;
			if (!idOwners.TryGetValue(entry.ComponentId, out var owners))
				idOwners[entry.ComponentId] = owners = new List<BootstrapEntry>();
			owners.Add(entry);
		}
		foreach (var kvp in idOwners)
		{
			if (kvp.Value.Count < 2)
				continue;
			var names = string.Join(", ", kvp.Value.Select(e => e.ComponentFullName));
			foreach (var dup in kvp.Value)
			{
				spc.ReportDiagnostic(Diagnostic.Create(
					new DiagnosticDescriptor(
						"VLT004",
						"Duplicate [ComponentId]",
						"Components {0} share the same [ComponentId(\"{1}\")]. Each component type must have a unique id — " +
						"give one of them a different id (e.g. \"{1}-2\").",
						"Voltage.Serialization",
						DiagnosticSeverity.Error,
						isEnabledByDefault: true),
					dup.DiagnosticLocation ?? Location.None,
					names, kvp.Key));
			}
		}

		foreach (var entry in unique)
		{
			// Always register the component factory
			sb.AppendLine($"\t\t\tComponentAotFactory.Register(");
			sb.AppendLine($"\t\t\t\ttypeof(global::{entry.ComponentFullName}).FullName,");
			sb.AppendLine($"\t\t\t\t() => new global::{entry.ComponentFullName}());");

			// Register the stable id → current component type so scenes resolve by identity.
			// AOT-safe: typeof(T) is a static token, no reflection.
			if (!string.IsNullOrEmpty(entry.ComponentId))
			{
				sb.AppendLine();
				sb.AppendLine($"\t\t\tglobal::Voltage.Serialization.ComponentIdRegistry.Register(");
				sb.AppendLine($"\t\t\t\t\"{EscapeString(entry.ComponentId)}\",");
				sb.AppendLine($"\t\t\t\ttypeof(global::{entry.ComponentFullName}));");
			}

			// Only emit deserializer for components with a MANUAL Data override.
			// Auto-generated components register their own deserializer via
			// __RegisterDeserializer_ emitted by EmitDataClass — which now uses
			// the JsonTokenReader-based reader, so it is fully NativeAOT-safe.
			if (entry.ManualDataTypeFullName != null)
			{
				sb.AppendLine();
				sb.AppendLine($"\t\t\tglobal::Voltage.Serialization.ComponentDataAotDeserializer.Register(");
				sb.AppendLine($"\t\t\t\ttypeof({entry.ManualDataTypeFullName}).FullName,");
				sb.AppendLine($"\t\t\t\tstatic _json => (global::Voltage.ComponentData)global::Voltage.Persistence.Json.FromJson(_json, typeof({entry.ManualDataTypeFullName})));");
			}

			// Emit TypeRenameRegistry registrations for every old name declared via
			// [FormerlyKnownAs] on this component. Both the component type name and its
			// generated/manual data type name are registered so that all serialized
			// occurrences of ComponentTypeName and DataTypeName survive the rename.
			// These calls are AOT-safe: typeof(T) is a static token, no reflection used.
			if (entry.FormerNames != null && entry.FormerNames.Count > 0)
			{
				sb.AppendLine();

				// The data class C# type expression for typeof():
				// - Auto-generated: global::ComponentFQN.ComponentNameGeneratedData
				//   (nested type accessed via dot in C#; '+' is only in reflection metadata)
				// - Manual override: entry.ManualDataTypeFullName (already fully-qualified, dots)
				string currentDataTypeExpr;
				if (entry.ManualDataTypeFullName != null)
				{
					currentDataTypeExpr = $"global::{entry.ManualDataTypeFullName}";
				}
				else
				{
					// Auto-generated: ComponentFQN.ComponentNameGeneratedData
					// Same convention as EmitDataClass: dataClassName = ClassName + "GeneratedData"
					var autoSuffix = GetSimpleClassName(entry.ComponentFullName) + "GeneratedData";
					currentDataTypeExpr = $"global::{entry.ComponentFullName}.{autoSuffix}";
				}

				for (int fi = 0; fi < entry.FormerNames.Count; fi++)
				{
					var oldName = entry.FormerNames[fi];

					// Register old ComponentTypeName → current component Type
					sb.AppendLine($"\t\t\tglobal::Voltage.Serialization.TypeRenameRegistry.Register(");
					sb.AppendLine($"\t\t\t\t\"{EscapeString(oldName)}\",");
					sb.AppendLine($"\t\t\t\ttypeof(global::{entry.ComponentFullName}));");

					// Register old DataTypeName → current data Type.
					// oldDataName is in reflection metadata form: OldComponentFQN+DataSuffix
					if (entry.FormerDataNames != null && fi < entry.FormerDataNames.Count)
					{
						var oldDataName = entry.FormerDataNames[fi];

						sb.AppendLine($"\t\t\tglobal::Voltage.Serialization.TypeRenameRegistry.Register(");
						sb.AppendLine($"\t\t\t\t\"{EscapeString(oldDataName)}\",");
						sb.AppendLine($"\t\t\t\ttypeof({currentDataTypeExpr}));");
					}
				}
			}

			sb.AppendLine();
		}

		sb.AppendLine("\t\t}");
		sb.AppendLine("\t}");
		sb.AppendLine("}");

		spc.AddSource("ComponentDataAotBootstrap.g.cs", SourceText.From(sb.ToString(), Encoding.UTF8));
	}

